package workspace2;
import java.util.*;
public class SumofNumWhile {

	public static void main(String[] args) {
		
		    {
		        int n = 10, sum = 0, i = 1;
		
		        while (i <= n) {

		            i++;
		        }
	
		        System.out.println(
		            "Sum of natural numbers using while loop is:"
		            + " " + sum);
		    }
		}
	}


